StageModel::StageModel() {
    
}

int StageModel::getStageCount(){
    return 11;
}

StageInfo* StageModel::getStage(int level){
    if(level >= 1 && level <= getStageCount()){
        return _stages[level - 1];
    }
    
    return _stages[0];
}