//////////////////////////////////////////////////////
//////////////////  StageInfo  ///////////////////////
//////////////////////////////////////////////////////

int StageInfo::getLevelCount(){
    return _levels.size();
}

LevelInfo* StageInfo::getLevelInfo(int level){
    if(level >= 1 && level <= getLevelCount()){
        return _levels[level - 1];
    }
    
    return _levels[0];
}

